# Нормализация: lower + ужать пробелы (для стабильного substring-поиска).
_NORMALIZE_RE = re.compile(r"\s+")


def _normalize(text: str) -> str:
    return _NORMALIZE_RE.sub(" ", text.lower())


def detect_category(title: str | None, description: str | None = None) -> str | None:
    """Возвращает основную категорию по сумме хитов ключевиков.

    Title весит больше, чем description (×3), потому что в title работодатели
    указывают самое важное. Если найдено несколько категорий с равным score —
    выигрывает та, что выше в словаре.
    """
    title_l = _normalize(title or "")
    desc_l = _normalize(description or "")

    if not title_l and not desc_l:
        return None

    scores: dict[str, int] = {}
    for category, keywords in CATEGORY_KEYWORDS.items():
        score = 0
        for kw in keywords:
            if kw in title_l:
                score += 3  # хиты в title весят больше
            if kw in desc_l:
                score += 1
        if score > 0:
            scores[category] = score

    if not scores:
        return None

    order = {cat: idx for idx, cat in enumerate(CATEGORY_KEYWORDS)}
    return max(scores.items(), key=lambda kv: (kv[1], -order[kv[0]]))[0]


def detect_categories(title: str | None, description: str | None = None) -> list[str]:
    """Возвращает все категории, под которые попадает вакансия (для аналитики).

    Полезно для админских графиков «сколько у нас в системе курьеров»,
    но не используется в Vacancy.category (там одно значение).
    """
    title_l = _normalize(title or "")
    desc_l = _normalize(description or "")
    if not title_l and not desc_l:
        return []

    matched: list[str] = []
    for category, keywords in CATEGORY_KEYWORDS.items():
        for kw in keywords:
            if kw in title_l or kw in desc_l:
                matched.append(category)
                break
    return matched
