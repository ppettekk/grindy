def normalize_city(raw: str) -> str:
    """Приводит ввод к канонической форме (lower, без 'г.', нормализован пробел)."""
    if not raw:
        return ""
    s = raw.strip().lower().replace("ё", "е")
    s = re.sub(r"^(г\.|город|гор\.)\s*", "", s)
    s = re.sub(r"\s+", " ", s)
    return s.strip(",. ")


def aliases_for(raw: str) -> list[str]:
    """Список вариантов имени города для LIKE-поиска.

    Если город в словаре — вернёт canonical + все алиасы.
    Если нет — только нормализованную форму.
    """
    norm = normalize_city(raw)
    if not norm:
        return []
    # Также проверяем «ё→е» в словаре (хотя там уже нормализовано выше).
    for canonical, alts in CITY_ALIASES.items():
        if norm == canonical or norm in alts:
            return [canonical, *[a for a in alts if a != canonical]]
    return [norm]
